function img(file) {
  return IMG + encodeURIComponent(file);
}

function galleryFrom(file, extra = []) {
  const urls = [file, ...extra].map(img);
  while (urls.length < 6) urls.push(urls[urls.length - 1]);
  return urls.slice(0, 6);
}

